# Arithematic Operators
x,y = 10,20
print(x+y) #30
print(x*y) #200
print(x-y) #-10
print(x/y) #0.5

# Relational Operators
print(x<y) #true
print(x>y) #false
print(x!=y) #true
print(x==y) #false
print(x<=y) #true
print(x>=y) #false

#Logical Operator

x,y = 5,4
print(x<8 & y<5) #true
print(x<8 & y<2) #false, because y is grester than 2
print(x<8 or y<2)#true 